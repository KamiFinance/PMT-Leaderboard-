export async function detectLanguage() {
  try {
    const r = await fetch('https://ipapi.co/json/', { signal: AbortSignal.timeout(4000) })
    const d = await r.json()
    return LANG_MAP[d.country_code] || 'en'
  } catch {
    return 'en'
  }
}

